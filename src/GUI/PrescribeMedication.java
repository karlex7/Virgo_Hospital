    private void LoadMedications() {
        DefaultListModel mod=new DefaultListModel();
        listMedications.setModel(mod);
        
        for (int i = 0; i < listaMed.size(); i++) {
            mod.addElement(listaMed.get(i).toString());
        }
     
    }

    private void prescribeMed() {
        int selected=listMedications.getSelectedIndex();
        patientWorkHandler.insertPrescribeMedication(new MedicationsPrescribed(listaMed.get(selected).getIDMedication(), IDPatient));
    }
